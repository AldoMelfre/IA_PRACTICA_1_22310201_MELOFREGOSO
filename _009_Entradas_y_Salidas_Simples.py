#A continuación, se muestra un ejemplo de un programa que solicita dos valores al usuario y luego muestra la suma, resta, multiplicación y división de los dos valores ingresados.
a = float(input("Ingresa el primer valor: "))
b = float(input("Ingresa el segundo valor: "))
print("Suma:", a + b)
print("Resta:", a - b)
print("Multiplicación:", a * b)
print("División:", a / b)
