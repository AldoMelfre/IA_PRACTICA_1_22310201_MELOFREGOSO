#EL operador NOT regresa el valor contrario booleano de la expresion que pasa como argumento, por un decir, si una expresion resulta verdadera, el operador not la convierte en falsa y viceversa.
#
#Ejemplo de uso de NOT
x = 15
10>x<20
print(not 10>20) #TRUE
print(not 10<20) #FALSE

#Ejemplo de uso de NOT
x = 15
y = 10
x = 15
print(not x==y) #TRUE, en escencia es false la respuesta pero el NOT invierte el valor
print(not x==x) #FALSE, en escencia es true la respuesta pero el NOT invierte el valor

