# La función len() devuelve la longitud de un objeto, como una lista, una cadena, un diccionario, etc.
# A continuación, se presentan varios ejemplos de cómo se puede utilizar la función len().
# Ejemplo con una lista
numbers = [1, 2, 3, 4, 5]
print(len(numbers))  # 5
# Ejemplo con una cadena de texto (string)
texto = "Hola, mundo"
print(len(texto))  # 11
# Ejemplo con una tupla
tupla = (1, 2, 3, 4, 5)
print(len(tupla))  # 5
# Ejemplo con un diccionario
diccionario = {'a': 1, 'b': 2, 'c': 3}
print(len(diccionario))  # 3

# La función len() es muy versátil y se puede aplicar a cualquier objeto que implemente el método especial __len__().

