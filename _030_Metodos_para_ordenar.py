#Existen muchos metodos para ordenar listas, algunos de ellos son:
#sort() - ordena la lista de menor a mayor
lst = [5, 3, 1, 2, 4]
print(lst)
 
lst.sort()
print(lst)  # output: [1, 2, 3, 4, 5]

#reverse() - ordena la lista de mayor a menor
lst = [5, 3, 1, 2, 4]
print(lst)
 
lst.reverse()
print(lst)  # output: [4, 2, 1, 3, 5]


#sort() - ordena la lista en orden alfabetico
lst = ["D", "F", "A", "Z"]
lst.sort()
 
print(lst)

#sort() - ordena la lista en orden alfabetico inverso
a = "A"
